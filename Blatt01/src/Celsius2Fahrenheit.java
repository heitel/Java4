
public class Celsius2Fahrenheit {
	public static void main(String[] args) {
		double celsius = -10;
		double fahrenheit = (9.0 * celsius) / 5 + 32;

		System.out
				.println("Celsius: " + celsius + " Fahrenheit: " + fahrenheit);

		celsius = 0;
		fahrenheit = (9.0 * celsius) / 5 + 32;

		System.out
				.println("Celsius: " + celsius + " Fahrenheit: " + fahrenheit);
		
		celsius = 25;
		fahrenheit = (9.0 * celsius) / 5 + 32;

		System.out
				.println("Celsius: " + celsius + " Fahrenheit: " + fahrenheit);

	}
}
